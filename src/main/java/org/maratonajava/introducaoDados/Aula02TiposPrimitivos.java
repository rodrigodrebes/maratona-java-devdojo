package org.maratonajava.introducaoDados;

public class Aula02TiposPrimitivos {
    public static void main(String[] args) {
        // int, double, float, char, byte, short, long, boolean
        // palavras reservadas em minúsculo

        int idadeDoPai;
        idadeDoPai = 50;
        idadeDoPai=60;
        System.out.println("A idade do meu pai é " + idadeDoPai + " anos");

        long numeroGrande = 1000000L;

        double salarioDouble = 2000.00;

        float salarioFloat = 2500.0F;

        byte idadeByte = 10;

        short idadeShort = 10;

        boolean verdadeiro = true;

        boolean falso = false;

        char caractere = 'M';
    }
}
